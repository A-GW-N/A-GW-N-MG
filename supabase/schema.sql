CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- -----------------------------------------------------------------------------
-- 1. 类型
-- -----------------------------------------------------------------------------

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1
    FROM pg_type t
    JOIN pg_namespace n ON n.oid = t.typnamespace
    WHERE t.typname = 'provider_type'
      AND n.nspname = 'public'
  ) THEN
    CREATE TYPE public.provider_type AS ENUM ('openai', 'gemini', 'anthropic');
  END IF;
END;
$$;

