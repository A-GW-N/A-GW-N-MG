CREATE OR REPLACE FUNCTION public.update_updated_at_column()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION public.validate_check_model_template_type()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
  template_type public.provider_type;
BEGIN
  IF NEW.template_id IS NULL THEN
    RETURN NEW;
  END IF;

  SELECT type INTO template_type
  FROM public.check_request_templates
  WHERE id = NEW.template_id;

  IF template_type IS NULL THEN
    RETURN NEW;
  END IF;

  IF template_type <> NEW.type THEN
    RAISE EXCEPTION '模板类型不匹配: model.type=%, template.type=%', NEW.type, template_type;
  END IF;

  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION public.validate_check_config_model_type()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
  linked_model_type public.provider_type;
BEGIN
  SELECT type INTO linked_model_type
  FROM public.check_models
  WHERE id = NEW.model_id;

  IF linked_model_type IS NULL THEN
    RETURN NEW;
  END IF;

  IF linked_model_type <> NEW.type THEN
    RAISE EXCEPTION '模型类型不匹配: config.type=%, model.type=%', NEW.type, linked_model_type;
  END IF;

  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION public.get_recent_check_history(
  limit_per_config integer DEFAULT 60,
  target_config_ids uuid[] DEFAULT NULL
)
RETURNS TABLE (
  config_id uuid,
  status text,
  latency_ms integer,
  ping_latency_ms integer,
  checked_at timestamptz,
  message text,
  name text,
  type text,
  model text,
  endpoint text,
  group_name text
)
LANGUAGE sql
STABLE
AS $$
  WITH ranked AS (
    SELECT
      h.id AS history_id,
      h.config_id,
      h.status,
      h.latency_ms,
      h.ping_latency_ms,
      h.checked_at,
      h.message,
      row_number() OVER (PARTITION BY h.config_id ORDER BY h.checked_at DESC) AS rn
    FROM public.check_history h
    WHERE target_config_ids IS NULL OR h.config_id = ANY(target_config_ids)
  )
  SELECT
    r.config_id,
    r.status,
    r.latency_ms,
    r.ping_latency_ms::integer,
    r.checked_at,
    r.message,
    c.name,
    c.type::text,
    m.model,
    c.endpoint,
    c.group_name
  FROM ranked r
  JOIN public.check_configs c ON c.id = r.config_id
  JOIN public.check_models m ON m.id = c.model_id
  WHERE r.rn <= limit_per_config
  ORDER BY c.name ASC, r.checked_at DESC;
$$;

DROP FUNCTION IF EXISTS public.prune_check_history(integer);

CREATE OR REPLACE FUNCTION public.prune_check_history(
  retention_days integer DEFAULT NULL,
  limit_per_config integer DEFAULT NULL
)
RETURNS integer
LANGUAGE plpgsql
VOLATILE
AS $$
DECLARE
  effective_days integer;
  deleted_count integer;
BEGIN
  effective_days := LEAST(365, GREATEST(7, COALESCE(retention_days, limit_per_config, 30)));

  DELETE FROM public.check_history
  WHERE checked_at < NOW() - (effective_days || ' days')::interval;

  GET DIAGNOSTICS deleted_count = ROW_COUNT;
  RETURN deleted_count;
END;
$$;

COMMENT ON FUNCTION public.prune_check_history IS '清理超过指定天数的历史记录，默认保留 30 天';

CREATE OR REPLACE FUNCTION public.list_pool_tables(pattern text DEFAULT '%-pool')
RETURNS TABLE (table_name text)
LANGUAGE sql
SECURITY DEFINER
SET search_path = public, information_schema
AS $$
  SELECT t.table_name::text
  FROM information_schema.tables t
  WHERE t.table_schema = 'public'
    AND t.table_type = 'BASE TABLE'
    AND t.table_name LIKE COALESCE(NULLIF(pattern, ''), '%-pool')
  ORDER BY t.table_name ASC;
$$;

COMMENT ON FUNCTION public.list_pool_tables IS '列出 public schema 中匹配 {name}-pool 命名模式的账号池数据表';

