CREATE OR REPLACE VIEW public.gateway_api_key_usage AS
SELECT
  k.id AS gateway_api_key_id,
  COUNT(l.id)::bigint AS request_count,
  COUNT(l.id) FILTER (WHERE l.success IS TRUE)::bigint AS success_count,
  COUNT(l.id) FILTER (WHERE l.success IS FALSE)::bigint AS failed_count,
  COALESCE(SUM(COALESCE(l.total_tokens, 0)), 0)::bigint AS total_tokens,
  COALESCE(SUM(COALESCE(l.input_tokens, 0)), 0)::bigint AS input_tokens,
  COALESCE(SUM(COALESCE(l.output_tokens, 0)), 0)::bigint AS output_tokens,
  MAX(l.created_at) AS last_used_at,
  COUNT(l.id) FILTER (WHERE l.created_at >= NOW() - INTERVAL '1 minute')::bigint AS rpm_count,
  COALESCE(SUM(COALESCE(NULLIF(l.total_tokens, 0), COALESCE(l.input_tokens, 0) + COALESCE(l.output_tokens, 0), 0)) FILTER (WHERE l.created_at >= NOW() - INTERVAL '1 minute'), 0)::bigint AS tpm_count
FROM public.gateway_api_keys k
LEFT JOIN public.gateway_request_logs l
  ON l.gateway_api_key_id = k.id
GROUP BY k.id;

COMMENT ON VIEW public.gateway_api_key_usage IS '按网关访问密钥聚合的请求用量统计视图';

CREATE OR REPLACE VIEW public.gateway_user_usage_summary AS
SELECT
  l.user_id,
  COUNT(l.id)::bigint AS request_count,
  COUNT(l.id) FILTER (WHERE l.success IS TRUE)::bigint AS success_count,
  COUNT(l.id) FILTER (WHERE l.success IS FALSE)::bigint AS failed_count,
  COALESCE(SUM(COALESCE(l.total_tokens, 0)), 0)::bigint AS total_tokens,
  COALESCE(SUM(COALESCE(l.input_tokens, 0)), 0)::bigint AS input_tokens,
  COALESCE(SUM(COALESCE(l.output_tokens, 0)), 0)::bigint AS output_tokens,
  MAX(l.created_at) AS last_request_at,
  COUNT(l.id) FILTER (WHERE l.created_at >= NOW() - INTERVAL '1 minute')::bigint AS rpm_count,
  COALESCE(SUM(COALESCE(NULLIF(l.total_tokens, 0), COALESCE(l.input_tokens, 0) + COALESCE(l.output_tokens, 0), 0)) FILTER (WHERE l.created_at >= NOW() - INTERVAL '1 minute'), 0)::bigint AS tpm_count
FROM public.gateway_request_logs l
WHERE l.user_id IS NOT NULL
GROUP BY l.user_id;

COMMENT ON VIEW public.gateway_user_usage_summary IS '按用户聚合的网关请求、token、RPM、TPM 统计视图';

CREATE OR REPLACE VIEW public.gateway_user_model_usage AS
SELECT
  l.user_id,
  COALESCE(NULLIF(BTRIM(l.mapped_model), ''), NULLIF(BTRIM(l.external_model), ''), 'unknown') AS model_name,
  COALESCE(NULLIF(BTRIM(l.brand), ''), 'other') AS brand,
  COUNT(l.id)::bigint AS request_count,
  COALESCE(SUM(COALESCE(l.total_tokens, 0)), 0)::bigint AS total_tokens,
  MAX(l.created_at) AS last_used_at
FROM public.gateway_request_logs l
WHERE l.user_id IS NOT NULL
GROUP BY
  l.user_id,
  COALESCE(NULLIF(BTRIM(l.mapped_model), ''), NULLIF(BTRIM(l.external_model), ''), 'unknown'),
  COALESCE(NULLIF(BTRIM(l.brand), ''), 'other');

COMMENT ON VIEW public.gateway_user_model_usage IS '按用户和模型聚合的用量统计视图';

